-- 01_create_portugal_rental_warehouse_lean.sql
-- Lean Portugal Rental Warehouse
--
-- Design goals:
-- - Keep only listings-level and reference data
-- - Exclude massive raw calendar/review event tables
-- - Exclude heavy daily/review fact tables
-- - Keep the database light enough for a laptop workflow

